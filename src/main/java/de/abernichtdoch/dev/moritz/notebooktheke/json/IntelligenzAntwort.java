package de.abernichtdoch.dev.moritz.notebooktheke.json;

public class IntelligenzAntwort {

    String hello;
    String intelligenz;

    public IntelligenzAntwort(String hello, String intelligenz) {
        this.hello = hello;
        this.intelligenz = intelligenz;
    }

    public String getHello() {
        return hello;
    }

    public String getIntelligenz() {
        return intelligenz;
    }

}
